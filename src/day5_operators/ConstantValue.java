package day5_operators;

public class ConstantValue {

	public static void main(String[] args) {
	
		final int numberOfUser = 4;
		
		System.out.println(numberOfUser);
		
		// numberOfUser = 8; // can not re assign the variable due to making it constant by adding 'final'
		
		System.out.println(numberOfUser);
		
		final float pi = 3.14f;
		
		final long daysInWeek = 7L;
		
		System.out.println(Integer.MAX_VALUE);
	
		long value = 2147483647L;
		
		
		int A = 10;
		int B = 3;
		int C = 4;
		int D = 5;
		
		System.out.println(A % B); // 1
		
		System.out.println(A % D); // 0
		System.out.println(A % C); // 2
		
		System.out.println(23 % 6);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
